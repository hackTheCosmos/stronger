    <h2>Collecte et utilisation des informations</h2>
    <p>Lorsque vous créez un compte utilisateur sur notre site web, nous pouvons collecter certaines informations personnelles vous concernant, telles que votre nom, votre adresse e-mail, et vos préférences en matière d'entraînement. Nous utilisons ces informations pour vous fournir un accès personnalisé à notre site web et pour améliorer nos services.</p>
   
    <h2>Cookies</h2>
    <p>Nous utilisons des cookies de session pour personnaliser votre expérience utilisateur. Un cookie est un petit fichier texte stocké sur votre ordinateur qui contient des informations permettant de vous identifier de manière unique. Vous pouvez configurer votre navigateur pour refuser tous les cookies ou pour vous alerter lorsque des cookies sont envoyés.</p>

    <h2>Protection des données</h2>
    <p>Nous mettons en œuvre des mesures de sécurité techniques et organisationnelles appropriées pour protéger vos données personnelles contre la perte, le vol, l'accès non autorisé, la divulgation, la modification ou la destruction. Cependant, aucune méthode de transmission sur Internet ou de stockage électronique n'est totalement sécurisée, et nous ne pouvons garantir la sécurité absolue de vos données.</p>

    <h2>Partage des informations</h2>
    <p>Nous ne vendons, ne louons ni ne partageons vos informations personnelles avec des tiers, sauf dans les cas suivants :</p>
    <ul>
        <li>Lorsque nous sommes légalement tenus de le faire, par exemple pour se conformer à une ordonnance du tribunal ou à une demande des autorités gouvernementales ;</li>
        <li>Lorsque vous avez donné votre consentement explicite pour le partage de vos informations.</li>
    </ul>

    <h2>Mineurs</h2>
    <p>Notre site web n'est pas destiné aux enfants de moins de 13 ans, et nous ne collectons pas sciemment d'informations personnelles auprès de personnes de moins de 13 ans. Si vous avez moins de 13 ans, veuillez ne pas utiliser notre site web ni nous fournir vos informations personnelles.</p>

    <h2>Modifications de la politique de confidentialité</h2>
    <p>Nous nous réservons le droit de modifier cette Politique de Confidentialité à tout moment, à notre seule discrétion. Les modifications entreront en vigueur dès leur publication sur notre site web. Il est de votre responsabilité de consulter régulièrement cette Politique de Confidentialité pour prendre connaissance des éventuelles modifications.</p>

    <h2>Contactez-nous</h2>
    <p>Si vous avez des questions ou des préoccupations concernant cette Politique de Confidentialité, veuillez nous contacter via le formulaire de contact du site accessible en suivant un lien dans le pied de page.</p>

