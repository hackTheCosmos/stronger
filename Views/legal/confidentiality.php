        <h1>Politique de Confidentialité</h1>

        <p>Chez <span>Stronger</span>, nous attachons une grande importance à la confidentialité et à la sécurité des informations de nos utilisateurs. La présente Politique de Confidentialité décrit comment nous collectons, utilisons et protégeons les données personnelles que vous nous fournissez lorsque vous utilisez notre site web et nos services.</p>
        
